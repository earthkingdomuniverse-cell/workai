export function getDealById(id: string): Deal | undefined {
  return mockDeals.find((deal) => deal.id === id);
}

export function getDealsByProviderId(providerId: string): Deal[] {
  return mockDeals.filter((deal) => deal.providerId === providerId);
}

export function getDealsByClientId(clientId: string): Deal[] {
  return mockDeals.filter((deal) => deal.clientId === clientId);
}

export function createDeal(
  dealData: Partial<Deal> & {
    providerId: string;
    clientId: string;
    title: string;
    description: string;
    amount: number;
  },
): Deal {
  const deal: Deal = {
    id: `deal_${Date.now()}`,
    providerId: dealData.providerId,
    clientId: dealData.clientId,
    status: 'created',
    title: dealData.title,
    description: dealData.description,
    amount: dealData.amount,
    currency: dealData.currency || 'USD',
    fundedAmount: 0,
    releasedAmount: 0,
    serviceFee: dealData.amount * 0.05,
    milestones: dealData.milestones || [],
    views: 0,
    timeline: [
      {
        id: `timeline_${Date.now()}`,
        type: 'status_change',
        title: 'Deal Created',
        userId: dealData.clientId,
        userName: 'System',
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
      },
    ],
    createdAt: new Date().toISOString(),
    updatedAt: new Date().toISOString(),
  };

  mockDeals.push(deal);
  return deal;
}
